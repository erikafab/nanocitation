package it.unipd.dei.nanocitation.web.model;


/**
 * This is the model class of the landing page
 * 
 * @author erika
 *
 */
public class LandingPage
{
	private String	textSnippet;
	private String	urlNP;
	private String	timeCreation;
	private String	rightHolder;
	private String	platform;
	private String	version;
	private String	creators;
	private String	authors;
	private String	assertion;
	private String	evidauthors;
	private String	provenance;
	private String idNP;
	
	

	public String getTextSnippet()
	{
		return textSnippet;
	}

	public void setTextSnippet(String textSnippet)
	{
		this.textSnippet = textSnippet;
	}

	public String getUrlNP()
	{
		return urlNP;
	}

	public void setUrlNP(String urlNP)
	{
		this.urlNP = urlNP;
	}

	public String getTimeCreation()
	{
		return timeCreation;
	}

	public void setTimeCreation(String timecreation)
	{
		this.timeCreation = timecreation;
	}

	public String getRightHolder()
	{
		return rightHolder;
	}

	public void setRightHolder(String rightholder)
	{
		this.rightHolder = rightholder;
	}

	public String getPlatform()
	{
		return platform;
	}

	public void setPlatform(String platform)
	{
		this.platform = platform;
	}

	public String getVersion()
	{
		return version;
	}

	public void setVersion(String version)
	{
		this.version = version;
	}

	public String getCreators()
	{
		return creators;
	}

	public void setCreators(String creators)
	{
		this.creators = creators;
	}

	public String getAuthors()
	{
		return authors;
	}

	public void setAuthors(String authors)
	{
		this.authors = authors;
	}

	public String getAssertion()
	{
		return assertion;
	}

	public void setAssertion(String assertion)
	{
		this.assertion = assertion;
	}

	public String getEvidauthors()
	{
		return evidauthors;
	}

	public void setEvidauthors(String evidauthors)
	{
		this.evidauthors = evidauthors;
	}

	public String getProvenance()
	{
		return provenance;
	}

	public void setProvenance(String provenance)
	{
		this.provenance = provenance;
	}

	public String getIdNP()
	{
		return idNP;
	}

	public void setIdNP(String idNP)
	{
		this.idNP = idNP;
	}

}
